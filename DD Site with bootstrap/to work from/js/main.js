//   video
let videoFile1 = document.getElementById('video-file1')
let videoButton1 = document.getElementById('video__button1')
let videoButtonIcon1 = document.getElementById('video__icon1')

function playPause1() {
    if (videoFile1.paused) {
        videoFile1.play()
        videoButtonIcon1.classList.add('ri-pause-circle-fill')
        videoButtonIcon1.classList.remove('ri-play-fill')
    } else {
        videoFile1.pause()
        videoButtonIcon1.classList.add('ri-play-fill')
        videoButtonIcon1.classList.remove('ri-pause-circle-fill')
    }
}
videoButton1.addEventListener('click', playPause1)

function finalVideo1() {
    videoButtonIcon1.classList.remove('ri-pause-circle-fill')
    videoButtonIcon1.classList.add('ri-play-fill')
}
videoFile1.addEventListener('ended', finalVideo1)

